char	*ft_strchr(const char *s, int c)
{
	unsigned char	chr;

	chr = c;
	while (*(s))
	{
		if (*(s) == chr)
			return ((char *)(s));
		s++;
	}
	if (c == 0)
		return ((char *)(s));
	return (0);
}
